interface Props { format: string; formatLabel?: string; }

export default function MenuTariffs({ format, formatLabel }: Props) {
  const tariffs = TARIFFS[format];
  if (!tariffs || tariffs.length === 0) return null;

  return (
    <section className="mb-12">
      <h2 className="text-xl font-heading font-medium mb-1">
        {formatLabel ? `Готовые предложения: ${formatLabel}` : 'Готовые предложения'}
      </h2>
      <p className="text-sm text-muted-foreground mb-6">
        Каждый тариф — полный состав блюд с описанием. Выберите готовый или{' '}
        <Link href={`/plan/constructor?format=${format}`} className="text-gold-text underline underline-offset-2">
          соберите свой
        </Link>.
      </p>

      <div className="space-y-6">
        {tariffs.map((t, i) => (
          <TariffBlock key={i} tariff={t} format={format} />
        ))}
      </div>
    </section>
  );
}

function TariffBlock({ tariff, format }: { tariff: MenuTariff; format: string }) {
  const [open, setOpen] = useState(false);

  return (
    <div className="rounded-2xl border-2 border-line bg-card overflow-hidden">
      <div className="p-5">
        <div className="flex items-start justify-between mb-2">
          <h3 className="font-heading text-lg font-semibold">{tariff.name}</h3>
          <span className="text-lg font-bold text-gold-text shrink-0 ml-4">{tariff.price}</span>
        </div>
        <p className="text-sm text-muted-foreground mb-3">{tariff.desc}</p>

        <div className="flex flex-wrap gap-1.5 mb-3">
          {tariff.highlights.map((h, j) => (
            <span key={j} className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full bg-secondary text-xs font-medium">
              <span className="text-gold-text">✦</span> {h}
            </span>
          ))}
        </div>

        <button
          onClick={() => setOpen(!open)}
          className="w-full flex items-center justify-between py-2 px-3 rounded-lg bg-secondary/30 hover:bg-secondary/50 transition-colors text-sm"
        >
          <span>{open ? 'Скрыть состав' : 'Показать состав меню'} ({tariff.dishes.reduce((s, c) => s + c.items.length, 0)} позиций)</span>
          <svg className={`w-4 h-4 transition-transform ${open ? 'rotate-180' : ''}`} fill="none" stroke="currentColor" strokeWidth="2" viewBox="0 0 24 24">
            <path d="M6 9l6 6 6-6" />
          </svg>
        </button>

        {open && (
          <div className="mt-4 space-y-3">
            {tariff.dishes.map((cat, j) => (
              <div key={j}>
                <h4 className="text-xs font-semibold uppercase tracking-wider text-muted-foreground mb-1.5">{cat.cat}</h4>
                <ul className="space-y-1.5">
                  {cat.items.map((item, k) => (
                    <li key={k} className="py-1 border-b border-line/20 last:border-0">
                      <div className="flex items-start justify-between gap-2">
                        <div className="min-w-0">
                          <span className="text-sm font-medium">{item.name}</span>
                          <p className="text-xs text-muted-foreground mt-0.5 leading-snug">{item.desc}</p>
                        </div>
                        <span className="text-xs text-muted-foreground shrink-0 mt-0.5">{item.qty}</span>
                      </div>
                    </li>
                  ))}
                </ul>
              </div>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}